#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	i1;

	i = 0;
	i1 = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[i1] != '\0')
	{
		s1[i + i1] = s2[i1];
		i1++;
	}
	s1[i + i1] = '\0';
	return (s1);
}
